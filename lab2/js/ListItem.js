class ListItem {
    /**
     * @param {string} id
     * @param {string} title
     * @param {string} content
     * @param {Date} dateLastEdited
     */
    constructor(id, title, content, dateLastEdited) {
        this.id = id
        this.title = title
        this.content = content
        this.dateLastEdited = dateLastEdited
    }
}
